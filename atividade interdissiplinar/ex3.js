let N = parseInt(prompt("Digite um número referente a um dia do mês"));

if(N==1){
    alert("Hoje é Domingo!");
}else if(N==2){
    alert("Hoje é Segunda!");
}else if(N==3){
    alert("Hoje é Terça!");
}else if(N==4){
    alert("Hoje é Quarta!");
}else if(N==5){
    alert("Hoje é Quinta!");
}else if(N==6){
    alert("Hoje é Sexta!");
}else if(N==7){
    alert("Hoje é Sábado!");
}            